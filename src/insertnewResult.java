    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollNo=jTextField1.getText();
        String math=jTextField2.getText();
        String cn=jTextField3.getText();
        String web=jTextField4.getText();
        String bio=jTextField5.getText();
        String se=jTextField6.getText();
        String ai=jTextField7.getText();
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","123456");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from student where rollNo='"+rollNo+"'");
            if(rs.next())
            {
                st.executeUpdate("insert into results (rollNo,math,cn,web,bio,se,ai) values('"+rollNo+"','"+math+"','"+cn+"','"+web+"','"+bio+"','"+se+"','"+ai+"')");
                JOptionPane.showMessageDialog(null,"Successfully Updated");
                setVisible(false);
                new insertnewResult().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Roll number does not exist in our database");
                setVisible(false);
                new insertnewResult().setVisible(true);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"connection error");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

