    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollNo=jTextField1.getText();
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","123456");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from student inner join results where student.rollNo='"+rollNo+"' and results.rollNo='"+rollNo+"'");
            if(rs.next())
            {
                setVisible(false);
                new studentHome(rollNo).setVisible(true);
            }
            else
                JOptionPane.showMessageDialog(null,"Incorrect roll number");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Connection error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

